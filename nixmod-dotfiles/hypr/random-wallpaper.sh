#!/usr/bin/env bash
# Writes hyprpaper config only. hyprland.conf starts hyprpaper after this.

WALLDIR="$HOME/Pictures/wallpapers"
CONF="$HOME/.config/hypr/hyprpaper.conf"
LAST_WALL="$HOME/.config/hypr/last_wallpaper.txt"

mkdir -p "$(dirname "$CONF")"

WALL=$(find "$WALLDIR" -type f \( -iname '*.jpg' -o -iname '*.png' \) 2>/dev/null | shuf -n 1)

if [[ -z "$WALL" ]]; then
  echo "❌ No wallpapers found in $WALLDIR" >&2
  exit 1
fi

# Avoid repeating same wallpaper twice in a row
if [[ -f "$LAST_WALL" ]]; then
  while [[ "$WALL" == "$(cat "$LAST_WALL" 2>/dev/null)" ]]; do
    WALL=$(find "$WALLDIR" -type f \( -iname '*.jpg' -o -iname '*.png' \) 2>/dev/null | shuf -n 1)
  done
fi

[[ "$WALL" != /* ]] && WALL="$(cd "$(dirname "$WALL")" 2>/dev/null && pwd)/$(basename "$WALL")"
echo "$WALL" > "$LAST_WALL"

# hyprpaper format: wallpaper { monitor = X; path = Y } (new block syntax, preload deprecated)
get_monitors() {
  if command -v jq &>/dev/null; then
    hyprctl -j monitors 2>/dev/null | jq -r '.[].name'
  else
    hyprctl monitors 2>/dev/null | awk '/Monitor/ {gsub(/:$/, "", $2); print $2}'
  fi
}

# At startup hyprctl may not be ready; retry briefly
for _ in 1 2 3 4 5; do
  MONS=($(get_monitors))
  [[ ${#MONS[@]} -gt 0 ]] && break
  sleep 0.5
done

{
  echo "# Autogenerated hyprpaper config"
  if [[ ${#MONS[@]} -gt 0 ]]; then
    for MON in "${MONS[@]}"; do
      echo "wallpaper {"
      echo "    monitor = $MON"
      echo "    path = $WALL"
      echo "    fit_mode = cover"
      echo "}"
    done
  else
    # Fallback: empty monitor = all outputs
    echo "wallpaper {"
    echo "    monitor ="
    echo "    path = $WALL"
    echo "    fit_mode = cover"
    echo "}"
  fi
} > "$CONF"
