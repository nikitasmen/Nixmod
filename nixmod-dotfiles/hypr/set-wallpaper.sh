#!/usr/bin/env bash

# Set a specific wallpaper in Hyprland using hyprpaper (IPC, no restart)
# Usage: ./set-wallpaper.sh [/path/to/wallpaper.jpg]
# If no path is provided, uses a random wallpaper from $HOME/Pictures/wallpapers

CONF="$HOME/.config/hypr/hyprpaper.conf"
LAST_WALL="$HOME/.config/hypr/last_wallpaper.txt"
WALLDIR="$HOME/Pictures/wallpapers"

# If no argument is provided, pick a random wallpaper from the default directory
if [[ -z "$1" ]]; then
    WALLPAPER=$(find "$WALLDIR" -type f \( -iname '*.jpg' -o -iname '*.png' \) 2>/dev/null | shuf -n 1)
    if [[ -z "$WALLPAPER" ]]; then
        echo "❌ No wallpapers found in $WALLDIR"
        exit 1
    fi
else
    WALLPAPER="$1"
fi

# Resolve to absolute path (hyprctl needs absolute or ~)
[[ "$WALLPAPER" != /* ]] && WALLPAPER="$(cd "$(dirname "$WALLPAPER")" 2>/dev/null && pwd)/$(basename "$WALLPAPER")"

# Check if the file exists
if [[ ! -f "$WALLPAPER" ]]; then
    echo "❌ Wallpaper not found: $WALLPAPER"
    exit 1
fi

# Create config directory if it doesn't exist
mkdir -p "$(dirname "$CONF")"

# Save the wallpaper path for future reference
echo "$WALLPAPER" > "$LAST_WALL"

# Write config file (used on next hyprpaper start / persistence)
echo "# Autogenerated hyprpaper config" > "$CONF"
echo "preload = $WALLPAPER" >> "$CONF"
for MON in $(hyprctl monitors 2>/dev/null | awk '/^Monitor/ {gsub(/:$/,"",$2); print $2}'); do
  echo "wallpaper = $MON,$WALLPAPER" >> "$CONF"
done

# Apply via IPC (no need to kill/restart hyprpaper)
if command -v hyprctl &>/dev/null; then
  hyprctl hyprpaper preload "$WALLPAPER" 2>/dev/null || true
  for MON in $(hyprctl monitors 2>/dev/null | awk '/^Monitor/ {gsub(/:$/,"",$2); print $2}'); do
    hyprctl hyprpaper wallpaper "$MON,$WALLPAPER" 2>/dev/null || true
  done
fi

# If IPC didn't work (e.g. hyprpaper not running), restart hyprpaper with new config
# DRI_PRIME=0 = use Intel iGPU (avoids NVIDIA EGL errors on Prime/hybrid)
if ! hyprctl hyprpaper listloaded &>/dev/null; then
  pkill -x hyprpaper 2>/dev/null || true
  sleep 0.5
  env DRI_PRIME=0 hyprpaper -c "$CONF" &
  sleep 0.5
  hyprctl hyprpaper preload "$WALLPAPER" 2>/dev/null || true
  for MON in $(hyprctl monitors 2>/dev/null | awk '/^Monitor/ {gsub(/:$/,"",$2); print $2}'); do
    hyprctl hyprpaper wallpaper "$MON,$WALLPAPER" 2>/dev/null || true
  done
fi

echo "✅ Wallpaper set to: $WALLPAPER"
